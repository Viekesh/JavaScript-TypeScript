// DNS stands for Domain Name System.
// It is primarily used to translate domain names into IP addresses.
// When you type a domain name, you must translate it into an IP address to send the traffic to the DNS server.
// So it takes that domain name, turns it into the IP address sends that data to process by the targeted server.
// Different DNS servers keep track of various web servers.

// DNS servers are not limited to converting domain names to IP addresses; they also store a fair amount of data
// in records.

// This includes records like A records and MX records.

// A records: They are domain names to IP address mappings.

// MX records: They are mapped to the mail server associated with a domain name.

